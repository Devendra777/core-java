package com.xworkz.app;

public class Wood {
	
	private int id ; 
	private String name ;
	
	
	public Wood() {
		System.out.println("Wood Constructor is invoked");
	}
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	} 
	
	
	

}
